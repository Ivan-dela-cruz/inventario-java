private void mnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnmodificarActionPerformed
// "CÓDIGO", "CÉDULA", "NOMBRES", "APELLIDOS", "DIRECCIÓN", "TELEFONO", "PLACA", "MODELO", "COLOR"    
    desbloquear();
    btnactualizar.setEnabled(true);
    btnguardar.setEnabled(false);
    int filamodificar = tb_materias.getSelectedRow();
    if (filamodificar >= 0) {
        txtcod.setText(tb_materias.getValueAt(filamodificar, 0).toString());
        txtnom.setText(tb_materias.getValueAt(filamodificar, 1).toString());
        txtdes.setText(tb_materias.getValueAt(filamodificar, 2).toString());
        spi_cantidad_inical.setValue(Integer.parseInt(tb_materias.getValueAt(filamodificar, 3).toString()));
        sp_stock.setValue(Integer.parseInt(tb_materias.getValueAt(filamodificar, 4).toString()));
        cboestado.setSelectedItem(tb_materias.getValueAt(filamodificar, 5).toString());
        this.txtcod.setEnabled(false);

    } else {
        JOptionPane.showMessageDialog(this, "No ha seleccionado ");
    }
}//GEN-LAST:event_mnmodificarActionPerformed

