    void cargar(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        String mostrar = "SELECT * FROM materias WHERE CONCAT(codigo,nombre,descripcion,cantidad_inicial,stock,estado) LIKE '%" + valor + "%'";
        String[] Titulos = {"CÓDIGO", "NOMBRE", "DESCRIPCIÓN", "CANTIDAD INICIAL", "STOCK", "ESTADO"};
        modelo.setColumnIdentifiers(Titulos);
        this.tb_materias.setModel(modelo);
        Conexion j = new Conexion();
        j.conectar();
        sql = j.sql;
        try {
            String[] registros = new String[6];
            s = sql.createStatement();
            ResultSet rs = s.executeQuery(mostrar);
            while (rs.next()) {
                registros[0] = rs.getString("codigo");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("descripcion");
                registros[3] = rs.getString("cantidad_inicial");
                registros[4] = rs.getString("stock");
                registros[5] = rs.getString("estado");
                modelo.addRow(registros);
            }
            tb_materias.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(MateriasPrima.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

