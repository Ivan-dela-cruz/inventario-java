private void mnenviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnenviarActionPerformed
// TODO add your handling code here:
    String cod = "", nom = "", des = "", stock = "";
    int fila = tb_productos.getSelectedRow();
    try {
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No ha seleccionado ningun dato");

        } else {
            //"CÓDIGO", "CÉDULA", "NOMBRES", "APELLIDOS", "DIRECCIÓN", "TELEFONO", "PLACA", "MODELO", "COLOR"
            cod = (String) tb_productos.getValueAt(fila, 0);
            nom = (String) tb_productos.getValueAt(fila, 1);
            des = (String) tb_productos.getValueAt(fila, 2);
            stock = (String) tb_productos.getValueAt(fila, 3);

            if (this.ventana.equals("ENTRADA")) {
                txtcodigo.setText(cod);
                txtnom.setText(nom);
                txtdes.setText(des);
                txtstock.setText(stock);
            } else {
                txtcodigo_s.setText(cod);
                txtnom_s.setText(nom);
                txtdes_s.setText(des);
                txtstock_s.setText(stock);

            }

            this.dispose();

        }
    } catch (Exception e) {
    }
}//GEN-LAST:event_mnenviarActionPerformed

