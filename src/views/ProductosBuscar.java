    void cargar(String valor) {

        DefaultTableModel modelo = new DefaultTableModel();
        String mostrar = "SELECT * FROM productos WHERE CONCAT(codigo,nombre,descripcion,stock) LIKE '%" + valor + "%' AND estado= 'ACTIVO'";
        String[] Titulos = {"CÓDIGO", "NOMBRE", "DESCRIPCIÓN", "STOCK"};
        modelo.setColumnIdentifiers(Titulos);
        this.tb_productos.setModel(modelo);
        Conexion j = new Conexion();
        j.conectar();
        sql = j.sql;
        try {
            String[] registros = new String[4];
            s = sql.createStatement();
            ResultSet rs = s.executeQuery(mostrar);
            while (rs.next()) {
                registros[0] = rs.getString("codigo");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("descripcion");
                registros[3] = rs.getString("stock");
                modelo.addRow(registros);
            }
            tb_productos.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(MateriasPrima.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

